//Overloading

package test2;

public class teststests {

	public static void main(String[] args) {
	
		
		addition(30,20);
		addition(20,10);
		addition(12,2);
		
	}	
		
	public static void addition(int w, int x) {
		System.out.println(w+x);
	}
	
	public static void addition(int w, int x, int y) {
		System.out.println(w+x+y);
	}
	
		
	public static void addition(int w, int x,int y, int z) {
		System.out.println(w+x+y+z);
	}
}