package test2;

public class Day22a {

	public static void main(String[] args) {
		
		
	addX(10,5);	
	addX(20,4);
	addX(30,6);
	
	
	}	
	//overloading	
		
	public static void addX(int a, int b) {
		System.out.println(a+b);
		
	}
	public static void addX(int a, int b, int c) {
		System.out.println(a+b+c);
		
		
	}	

	public static void addX(int a, int b, int c, int d) {
		System.out.println(a+b+c+d);
		
	}

}